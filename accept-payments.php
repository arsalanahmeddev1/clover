              <div id="collapseTwo" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                <div class="accordion-body">
                  <p class="graphik-medium mb-10 fs-18 mb-10">Payment processing refers to the <a href="" class="primary-hover text-decoration-underline">facilitation of the transfer of funds</a> from a customer's credit card or bank account to a business’s merchant account. Here's how it works:</p>
                  <p class="graphik-medium mb-10 fs-18 mb-10"><strong>Transmission:</strong>When a customer makes a purchase, their payment details are encrypted and transmitted to the payment processor who sends it to the card network.</p>
                  <p class="graphik-medium mb-10 fs-18 mb-10"><strong>Authorization</strong>
                    The customer’s card-issuing bank verifies the customer's information and checks if they have sufficient funds to complete the transaction.
                  </p>
                  <p class="graphik-medium mb-10 fs-18 mb-10"><strong>Notification</strong>The consumer’s bank sends a notification back to the processor and the merchant informing them if the transaction is approved or declined.</p>
                  <p class="graphik-medium mb-10 fs-18 mb-10"><strong>Completion</strong>If approved, the merchant completes the transaction by issuing a receipt. For eCommerce orders, the consumer receives a receipt and order confirmation.</p>
                  <p class="graphik-medium mb-10 fs-18"><strong>Settlement</strong>The processor’s acquiring bank collects the funds from the consumer’s card-issuing bank. These funds are then deposited into the merchant’s bank account, less fees, within 1-3 business days depending on the card network involved.</p>
